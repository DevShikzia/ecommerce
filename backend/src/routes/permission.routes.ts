/**
 * Rutas de permisos
 * Endpoints para gestión de permisos (protegidos por middleware de permisos)
 */
import { Router, Request, Response } from 'express';
import { permissionController } from '../controllers/permission.controller';
import { checkPermission } from '../middleware/permisos.middleware';
import { authMiddleware, AuthenticatedRequest } from '../middleware/auth.middleware';

const router = Router();

router.use(authMiddleware);
router.use(checkPermission);

router.get('/', (req: Request, res: Response) => permissionController.getPermissions(req as AuthenticatedRequest, res));
router.get('/auto-generated', (req: Request, res: Response) => permissionController.getAutoGenerated(req as AuthenticatedRequest, res));
router.get('/:id', (req: Request, res: Response) => permissionController.getPermissionById(req as AuthenticatedRequest, res));
router.post('/', (req: Request, res: Response) => permissionController.createPermission(req as AuthenticatedRequest, res));
router.put('/:id', (req: Request, res: Response) => permissionController.updatePermission(req as AuthenticatedRequest, res));
router.delete('/:id', (req: Request, res: Response) => permissionController.deletePermission(req as AuthenticatedRequest, res));

export default router;